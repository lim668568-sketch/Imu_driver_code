uint8_t CRC::calculateCRC8(const QByteArray &data, int start, int length)
{
    if (length < 0) {
        length = data.size() - start;
    }
    
    uint8_t crc = 0;
    for (int i = start; i < start + length; ++i) {
        crc = CRC8_TABLE[crc ^ static_cast<uint8_t>(data[i])];
    }
    return crc;
}

uint16_t CRC::calculateCRC16(const QByteArray &data, int start, int length)
{
    if (length < 0) {
        length = data.size() - start;
    }
    
    uint16_t crc = 0xFFFF;
    for (int i = start; i < start + length; ++i) {
        crc = (crc >> 8) ^ CRC16_TABLE[(crc ^ static_cast<uint8_t>(data[i])) & 0xFF];
    }
    return crc;
}

uint32_t CRC::calculateCRC32(const QByteArray &data, int start, int length)
{
    if (length < 0) {
        length = data.size() - start;
    }
    
    uint32_t crc = 0xFFFFFFFF;
    for (int i = start; i < start + length; ++i) {
        crc = (crc >> 8) ^ CRC32_TABLE[(crc ^ static_cast<uint8_t>(data[i])) & 0xFF];
    }
    return crc ^ 0xFFFFFFFF;
}

bool CRC::verifyCRC8(const QByteArray &data, uint8_t crc)
{
    return calculateCRC8(data) == crc;
}

bool CRC::verifyCRC16(const QByteArray &data, uint16_t crc)
{
    return calculateCRC16(data) == crc;
}

bool CRC::verifyCRC32(const QByteArray &data, uint32_t crc)
{
    return calculateCRC32(data) == crc;
}

QByteArray CRC::addCRC8(QByteArray &data)
{
    uint8_t crc = calculateCRC8(data);
    data.append(static_cast<char>(crc));
    return data;
}

QByteArray CRC::addCRC16(QByteArray &data)
{
    uint16_t crc = calculateCRC16(data);
    data.append(static_cast<char>(crc & 0xFF));
    data.append(static_cast<char>((crc >> 8) & 0xFF));
    return data;
}

QByteArray CRC::addCRC32(QByteArray &data)
{
    uint32_t crc = calculateCRC32(data);
    data.append(static_cast<char>(crc & 0xFF));
    data.append(static_cast<char>((crc >> 8) & 0xFF));
    data.append(static_cast<char>((crc >> 16) & 0xFF));
    data.append(static_cast<char>((crc >> 24) & 0xFF));
    return data;
}

